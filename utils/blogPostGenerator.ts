// Generate meta tags for SEO
export function generateMetaTags(post: GeneratedBlogPost) {
  return {
    title: post.metaTitle || post.title.substring(0, 60),
    description: post.metaDescription || post.description.substring(0, 160),
    keywords: post.keywords.join(', '),
    ogTitle: post.title,
    ogDescription: post.description,
    ogImage: post.imageUrl,
    canonicalUrl: `/blog/${post.slug}`
  };
}

